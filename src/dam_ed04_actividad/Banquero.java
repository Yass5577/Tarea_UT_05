public class Banquero {
    String nombre;
    String apellidos;
    String dni;

    public Banquero() {
        nombre = null;
        apellidos = null;
        dni = null;
    }

    public Banquero(String nombre, String apellidos, String dni) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.dni = dni;
    }
    
    
}
